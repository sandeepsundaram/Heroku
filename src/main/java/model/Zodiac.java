package model;

public class Zodiac {
	private String bio = null;
	private String name = null;
	private String sunSign = null;
	private String chineseSign = null;
	private String element = null;
	private String charactor = null;	
	
	public String getBio() {
		return bio;
	}
	public void setBio(String bio) {
		this.bio = bio;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCharactor() {
		return charactor;
	}
	public void setCharactor(String charactor) {
		this.charactor = charactor;
	}
	public String getSunSign() {
		return sunSign;
	}
	public void setSunSign(String sunSign) {
		this.sunSign = sunSign;
	}
	public String getChineseSign() {
		return chineseSign;
	}
	public void setChineseSign(String chineseSign) {
		this.chineseSign = chineseSign;
	}
	public String getElement() {
		return element;
	}
	public void setElement(String element) {
		this.element = element;
	}
	
}
